void main() {
  /* Oprasi Aritmatika
  kita juga bisa menggunakan operasi aritmatika di dart */

  var angka1 = 1156;
  var angka2 = 246;

  print(angka1 + angka2); // ::  +  (penjumblahan)
  print(angka1 - angka2); // ::  -  (pengurangan)
  print(angka1 * angka2); // ::  *  (perkalian)
  print(angka1 / angka2); // ::  /  (pembagian double)
  print(angka1 ~/ angka2); // ::  ~/ (pembagian int)
  print(angka1 % angka2); // ::  %  (modulus sisa bagi)
}
